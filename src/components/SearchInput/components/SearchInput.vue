<script lang="ts">

import { defineComponent } from 'vue';

export default defineComponent({
  props: {
    placeholder: {
      type: String,
    },

    value: {
      type: String,
    },
  },

  setup(_, { emit }) {
    function handleValueChange(e) {
      emit('input', (e.target as HTMLInputElement).value);
    }

    return {
      handleValueChange,
    };
  },
});

</script>

<template>
  <div>
    <div class="absolute inset-y-0 left-0 flex items-center pl-3 pointer-events-none">
      <svg class="w-5 h-5 text-gray-400" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" fill="currentColor">
        <path fill-rule="evenodd" d="M8 4a4 4 0 100 8 4 4 0 000-8zM2 8a6 6 0 1110.89 3.476l4.817 4.817a1 1 0 01-1.414 1.414l-4.816-4.816A6 6 0 012 8z" clip-rule="evenodd"></path>
      </svg>
    </div>
    <input type="text" @input="handleValueChange" :value="value" class="w-full shadow-sm px-3 py-2 pl-10 bg-white border border-gray-300 appearance-none focus:ring-2 focus:outline-none focus:border-blue-500" :placeholder="placeholder" />
  </div>
</template>
